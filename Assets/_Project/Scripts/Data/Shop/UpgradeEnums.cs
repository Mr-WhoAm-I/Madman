namespace _Project.Scripts.Data.Shop
{
    // Тематические редкости (Tier 1 - Tier 4)
    public enum UpgradeRarity
    {
        Ordinary,      // Ординарный (Серый) - Базовые статы
        Experimental,  // Экспериментальный (Зеленый) - Продвинутые статы
        Banned,        // Запрещенный (Фиолетовый) - Меняющие механику
        Anomalous      // Аномальный (Красный) - Уникальные классовые
    }

    // Типы модификаторов, которые может менять карточка
    public enum UpgradeType
    {
        // === БАЗОВЫЙ ПУЛ ===
        FlatDamage,         // +X к базовому урону
        MaxHealth,          // +X% к максимальному здоровью
        MoveSpeed,          // +X% к скорости бега
        CritChance,         // +X% шанс крита
        CritDamage,         // +X% урон крита
        MagnetRadius,       // +X к радиусу сбора лута
        CooldownReduction,  // -X% времени перезарядки навыка
        MaxRerolls,         // Увеличение количества рероллов (база = 1)
        MinDiscount,        // Увеличение минимальной скидки (0.1, 0.2 и т.д.)
        
        // === ОРУЖЕЙНЫЕ ===
        PierceCount,        // +X пробитий насквозь
        RicochetCount,      // +X отскоков
        ExtraProjectiles,   // +X пуль за выстрел (дробовик)
        Lifesteal,          // +X% вампиризма
        
        // === КЛАССОВЫЕ: ПАРАНОИК ===
        ShieldRechargeTime, // Уменьшение таймера вне боя для щита
        ShieldReflect,      // Возврат урона от щита
        TurretExplode,      // Флаг взрыва турели при смерти
        MaxTurrets,         // Увеличение капа турелей
        TurretCryo,         // Флаг замораживающих снарядов турели
        TurretHealAura,     // Лечение союзников вокруг турели (ХП в секунду)
        
        // === КЛАССОВЫЕ: ШИЗОИД ===
        MaxInstability,     // Максимальное количество стаков нестабильности
        CloneRadiusMult,    // Множитель радиуса взрыва клона
        MiniClones,         // Спавн мини-клонов после взрыва
        InvisDuration,      // Увеличение времени невидимости
        CloneToxicCloudDPS,   // Урон ядовитого облака клона в секунду
        CloneShootingMult,    // Множитель урона клона от базового (включает стрельбу)
        ShadowStrikeMult,     // Множитель урона для первого выстрела из инвиза
        ClonePoisonDPS,       // Урон от яда при попадании пули клона (Истязание)
        InvisSpeedMult,       // Множитель скорости бега в инвизе (Паркур)
        KillCooldownReduction, // Сокращение КД ульты за убийство в секундах
        
        // === КЛАССОВЫЕ: МЕЛАНХОЛИК ===
        FreezeDuration,     // Увеличение длительности заморозки
        ApathyMaxStacks,    // Снижение требуемых стаков для заморозки
        ChainTargets,       // Увеличение числа целей для цепной ульты
        ShrapnelDeath,      // Взрыв шипами при смерти замороженного врага
        FrostVulnerability, // Увеличение урона по замороженным целям
        
        // === КЛАССОВЫЕ: ИСТЕРИК ===
        FuryThreshold,       // Изменение порога ХП для входа в Ярость
        ForceFuryOnUltimate, // Флаг для навыка "Перегрузка"
        TornadoMultiplier,    // Множитель для навыка "Смерч"
        
        AuraRadius,         // ДОБАВЛЕНО: Радиус Ауры уныния
        ShieldPerFreeze     // ДОБАВЛЕНО: Процент щита за заморозку (Ледяной доспех)
    }
}